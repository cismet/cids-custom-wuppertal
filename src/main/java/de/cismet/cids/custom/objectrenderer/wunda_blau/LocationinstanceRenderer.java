    @Override
    public void setCidsBean(CidsBean cidsBean) {
        if (cidsBean != null) {
            this.cidsBean = cidsBean;
            bindingGroup.unbind();
            bindingGroup.bind();
            initLocationTypeImages();
            if (lblMail.getText().length() < 3) {
                lblMail.setVisible(false);
            }
            if (lblUrl.getText().length() < 3) {
                lblUrl.setVisible(false);
            }
        }
    }

