    /**
     * DOCUMENT ME!
     *
     * @version  $Revision$, $Date$
     */
    final class PictureWorker extends SwingWorker<Void, ImageIcon> {

        //~ Instance fields ----------------------------------------------------

        private int i = -1;
        private String[] urls;
        private Collection<CidsBean> beans;

        //~ Constructors -------------------------------------------------------

        /**
         * Creates a new PictureWorker object.
         *
         * @param  beans  DOCUMENT ME!
         */
        public PictureWorker(final Collection<CidsBean> beans) {
            this.beans = beans;
        }

        //~ Methods ------------------------------------------------------------

        /**
         * DOCUMENT ME!
         *
         * @return  DOCUMENT ME!
         *
         * @throws  Exception  DOCUMENT ME!
         */
        @Override
        protected Void doInBackground() throws Exception {
            int j = -1;
            urls = new String[beans.size()];
            for (final CidsBean b : beans) {
                if (isCancelled()) {
                    return null;
                }
                ImageIcon ii = null;
                final String bnr = (String)b.getProperty("bildnummer");
                if (bnr != null) {
                    final String url = StadtbilderUtils.getArcUrlPath(bnr);
                    urls[++j] = url;
                    ii = ObjectRendererUtils.loadPicture(url, 300, 300, 4);
                }
                if (ii == null) {
                    ii = ERROR_ICON;
                }
                publish(ii);
            }
            return null;
        }

        /**
         * DOCUMENT ME!
         */
        @Override
        protected void done() {
            try {
                get();
            } catch (InterruptedException ex) {
                log.warn(ex, ex);
            } catch (ExecutionException ex) {
                log.error(ex, ex);
            }
        }

        /**
         * DOCUMENT ME!
         *
         * @param  chunks  DOCUMENT ME!
         */
        @Override
        protected void process(final List<ImageIcon> chunks) {
            if (chunks != null) {
                for (final ImageIcon ii : chunks) {
                    if (!isCancelled()) {
                        if (++i < imgLabels.length) {
                            final JLabel label = imgLabels[i];
                            if (label != null) {
                                label.setIcon(ii);
                                ObjectRendererUtils.decorateComponentWithMouseOverCursorChange(
                                    label,
                                    Cursor.HAND_CURSOR,
                                    Cursor.DEFAULT_CURSOR);
                                if (ii != ERROR_ICON) {
                                    label.addMouseListener(new MouseAdapter() {

                                            private final String u = urls[i];

                                            @Override
                                            public void mouseClicked(final MouseEvent e) {
                                                if (!e.isPopupTrigger()) {
                                                    if (u != null) {
                                                        ObjectRendererUtils.openURL(u);
                                                    }
                                                }
                                            }
                                        });
                                }
                            }
                        }
                    }
                }
            }
        }
    }
