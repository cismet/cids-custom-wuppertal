    @Override
    public boolean prepareForSave() {
        boolean save = true;
        final StringBuilder errorMessage = new StringBuilder();

        // name vorhanden
        try {
            if (txtName.getText().trim().isEmpty()) {
                LOG.warn("No name specified. Skip persisting.");
                errorMessage.append(NbBundle.getMessage(
                        PrbrBahnlinieEditor.class,
                        "PrbrBahnlinieEditor.prepareForSave().noName"));
            } else {
                final String myQuery;
                myQuery = " where name ilike '" + txtName.getText().trim() + "'";
                if (TableUtils.getOtherTableValue(TABLE_NAME, myQuery, getConnectionContext()) != null) {
                    LOG.warn("Duplicate name specified. Skip persisting.");
                    errorMessage.append(NbBundle.getMessage(
                            PrbrBahnlinieEditor.class,
                            "PrbrBahnlinieEditor.prepareForSave().duplicateName"));
                } else {
                    cidsBean.setProperty("schluessel", txtName.getText().trim());
                }
            }
        } catch (final Exception ex) {
            LOG.warn("Name not given.", ex);
            save = false;
        }

        if (errorMessage.length() > 0) {
            JOptionPane.showMessageDialog(StaticSwingTools.getParentFrame(this),
                NbBundle.getMessage(
                    PrbrBahnlinieEditor.class,
                    "PrbrBahnlinieEditor.prepareForSave().JOptionPane.message.prefix")
                        + errorMessage.toString()
                        + NbBundle.getMessage(
                            PrbrBahnlinieEditor.class,
                            "PrbrBahnlinieEditor.prepareForSave().JOptionPane.message.suffix"),
                NbBundle.getMessage(
                    PrbrBahnlinieEditor.class,
                    "PrbrBahnlinieEditor.prepareForSave().JOptionPane.title"),
                JOptionPane.WARNING_MESSAGE);

            return false;
        }
        return save;
    }

