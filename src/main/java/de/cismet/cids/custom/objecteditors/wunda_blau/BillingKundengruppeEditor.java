    @Override
    public void beansDropped(final ArrayList<CidsBean> beans) {
        final List<CidsBean> customersOfTheGroup = cidsBean.getBeanCollectionProperty("kunden_arr");
        for (final CidsBean bean : beans) {
            if (bean.getMetaObject().getMetaClass().getTableName().equalsIgnoreCase("billing_kunde")
                        && !customersOfTheGroup.contains(bean)) {
                customersOfTheGroup.add(bean);
            }
        }
    }
