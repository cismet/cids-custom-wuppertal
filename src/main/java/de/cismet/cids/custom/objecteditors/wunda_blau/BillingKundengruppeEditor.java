    @Override
    public String getTitle() {
        final String cidsBeanName = String.valueOf(cidsBean);
        String title;
        if (cidsBeanName.equals("null")) {
            title = "neue Kundengruppe";
        } else {
            title = "Kundengruppe: " + cidsBeanName;
        }
        return title;
    }

