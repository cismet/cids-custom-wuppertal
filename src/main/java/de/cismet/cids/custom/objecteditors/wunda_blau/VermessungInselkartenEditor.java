    /**
     * DOCUMENT ME!
     *
     * @return  DOCUMENT ME!
     */
    @Override
    protected String getDocumentFilename() {
        final CidsBean cidsBean = getCidsBean();
        final String schluessel = (String)cidsBean.getProperty("schluessel");
        final Integer gemarkung = (Integer)cidsBean.getProperty("gemarkung.id");
        final Integer flur = Integer.parseInt((String)cidsBean.getProperty("flur"));
        final Integer blatt = (Integer)cidsBean.getProperty("blatt");
        final String version = (String)cidsBean.getProperty("version");
        return VermessungPictureFinderClientUtils.getInselkartenPictureFilename(
                schluessel,
                gemarkung,
                flur,
                blatt,
                version);
    }

    /**
     * DOCUMENT ME!
     *
     * @param  cidsBean  DOCUMENT ME!
     */
    @Override
    public void setCidsBean(final CidsBean cidsBean) {
        super.setCidsBean(cidsBean);
        bindingGroup.unbind();

        if (cidsBean != null) {
            DefaultCustomObjectEditor.setMetaClassInformationToMetaClassStoreComponentsInBindingGroup(
                bindingGroup,
                cidsBean,
                getConnectionContext());
            bindingGroup.bind();
        }
    }

    @Override
    protected String findPicture() {
        final CidsBean cidsBean = getCidsBean();
        final String schluessel = (String)cidsBean.getProperty("schluessel");
        final Integer gemarkung = (Integer)cidsBean.getProperty("gemarkung.id");
        final Integer flur = Integer.parseInt((String)cidsBean.getProperty("flur"));
        final Integer blatt = (Integer)cidsBean.getProperty("blatt");
        final String version = (String)cidsBean.getProperty("version");

        return VermessungPictureFinderClientUtils.findInselkartenPicture(
                schluessel,
                gemarkung,
                flur,
                blatt,
                version);
    }

    /**
     * DOCUMENT ME!
     *
     * @return  DOCUMENT ME!
     */
    @Override
    protected String generateTitle() {
        final CidsBean cidsBean = getCidsBean();
        final String schluessel = (String)cidsBean.getProperty("schluessel");
        final String flur = (String)cidsBean.getProperty("flur");
        final Integer blatt = (Integer)cidsBean.getProperty("blatt");
        final String version = (String)cidsBean.getProperty("version");
        final String gemarkung = (String)cidsBean.getProperty("gemarkung.name");

        return new StringBuilder().append("SchlÃ¼ssel ")
                    .append(((schluessel != null) && (schluessel.trim().length() > 0)) ? schluessel : "unbekannt")
                    .append(" - ")
                    .append("Gemarkung ")
                    .append((gemarkung != null) ? gemarkung : "unbekannt")
                    .append(" - ")
                    .append("Flur ")
                    .append((flur != null) ? flur : "unbekannt")
                    .append(" - ")
                    .append("Blatt ")
                    .append((blatt != null) ? blatt : "unbekannt")
                    .append(" - ")
                    .append("Version ")
                    .append((version != null) ? version : "unbekannt")
                    .toString();
    }

